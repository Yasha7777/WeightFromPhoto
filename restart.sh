# 1. Жестко убиваем старый скрипт по имени
pkill -f receiver_dust3r

# 2. На всякий случай освобождаем порт, если его кто-то держит
fuser -k 6006/tcp

# 3. Даем операционной системе 2 секунды полностью освободить ресурсы
sleep 2

# 4. Создаем папку под логи, если её ещё нет (предохранитель)
mkdir -p logs

# 5. Запускаем ОДИН экземпляр в фоне, перенаправив и стандартный вывод (1), и ошибки (2) в файл
nohup python3 receiver_dust3r.py > logs/dust3r.log 2>&1 &

# 6. Включаем живой просмотр лога в реальном времени
tail -f logs/dust3r.log
